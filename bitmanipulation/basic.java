package bitmanipulation;
import java.util.*;


//learn bitwise operator

public class basic {
    public static void main(String[] args) {
        System.out.println(5&6); //and opertor
        System.out.println(5|6); //or operator
        System.out.println(5^6); // xor operator
        System.out.println(~5); //binary one's complement
        System.out.println(5<<2); //binary left shift  formula -> a<<b=a *2^b
        System.out.println(6>>1); //binary right shift  formula -> a>>b=a/2^b

        
    }
    
}
